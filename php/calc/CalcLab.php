<?php

/**
 * Class CalcLab
 */
class CalcLab
{
    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function soma($a, $b)
    {
        return $a + $b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function subtracao($a, $b)
    {
        return $a + $b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function divisao($a, $b)
    {
        return $a / $b;
    }

    /**
     * @param float $a
     * @param float $b
     * @return float
     */
    public function mult( $a,  $b)
    {
        return $a * $b;
    }
}
